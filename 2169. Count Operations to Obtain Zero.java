    public int countOperations(int num1, int num2) {
        int count = 1;
        if (num1 == num2 && num2 == 0) {
            return 0;
        } else if (num1 == num2) {
            return 1;
        } else if (num1 == 0 || num2 == 0) {
            return 0;
        }
        while (num1 != num2) {
            if (num1 > num2) {
                num1 = num1 - num2;
            } else {
                num2 = num2 - num1;
            }
            count++;
        }
        return count == 1 ? 0 : count;
    }

